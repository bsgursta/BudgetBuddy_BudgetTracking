const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const userSchema = new Schema({
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    profile: {
        type: String,
        default: '' //placeholder for profile
    }

},{timestamps:true})

const User = mongoose.model('User',userSchema);
module.exports = User;